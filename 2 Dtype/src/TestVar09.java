/**
 * 布尔型变量
 */


public class TestVar09 {
    public static void main(String[] args) {
        // 创建布尔型变量
        boolean flag1 = true;
        System.out.println(flag1);
        boolean flag2 = false;
        System.out.println(flag2);
        boolean flag3 = 5 == 9;
        System.out.println(flag3);
        boolean flag4 = 5 < 9;
        System.out.println(flag4);
    }
}
